package com.perficient.hr.form;

/**
 * @author praveen.muthusamy
 *
 */
public class Status {

	private int open;
	private int completed;
	private int notApplicable;

	/**
	 * @return the open
	 */
	public int getOpen() {
		return open;
	}

	/**
	 * @param open
	 *            the open to set
	 */
	public void setOpen(int open) {
		this.open = open;
	}

	/**
	 * @return the completed
	 */
	public int getCompleted() {
		return completed;
	}

	/**
	 * @param completed
	 *            the completed to set
	 */
	public void setCompleted(int completed) {
		this.completed = completed;
	}

	/**
	 * @return the notApplicable
	 */
	public int getNotApplicable() {
		return notApplicable;
	}

	/**
	 * @param notApplicable
	 *            the notApplicable to set
	 */
	public void setNotApplicable(int notApplicable) {
		this.notApplicable = notApplicable;
	}

}
